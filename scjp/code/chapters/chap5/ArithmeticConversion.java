
 public class ArithmeticConversion{
     public static void main(String[] args) {
       byte b = 5;
       int i = 15;
       float f =   2.0f; 
       double d = 10.5d;
       if ( (d - f ) >= (-b*i) ) {
         System.out.println("The condition >= is true.");
      } else {
         System.out.println("The condition >= is false.");
      }      
   }
 }